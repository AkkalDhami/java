public class largest_of_three_num {
    public static void main(String[] args) {
        int x = -5, y = 9, z = 9;
        if (x > y) {
            System.out.println(x + " is the largest number.");
        } else if (y > z) {
            System.out.println(y + " is the largest number.");
        } else {
            System.out.println(z + " is the largest number.");
        }
    }
}
